public class shisoku {
    public static void main(String[] args) {
        int x = 3;

        int y = x + 5;
        int z = x - 13;
        int v = x * 3;
        int w = x / 3;
        int w2 = x % 2;

        System.out.println("x = " + x);
        System.out.println("x + 5 = " + y);
        System.out.println("x - 13 = " + z);
        System.out.println("x * 3 = " + v);
        System.out.println("x / 3 = " + w);
        System.out.println("x % 2 = " + w2);
    }
}
