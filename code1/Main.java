public class Main {
    public static void main(String[] args){

        final double PI = 3.14; // 定数
        int pie = 5;

        System.out.println("半径" + pie + "cmのパイの面積は，");
        System.out.println(pie * pie * PI);
        System.out.println("パイの半径を倍にします");

        // エラーになる PI = 10;

        System.out.println("半径" + pie + "cmのパイの面積は，");
        System.out.println(pie * pie * PI);

    }
}
